package com.negildo.loopfor;

public class For {
    public static void main(String[] args) {


        for (int i = 0; i<10; i++ ){
            System.out.println("I tem valor :" + i);
        }

         for (int i=0,  j=10; i<j ; i++, j--){
             System.out.println("i: " +i + " j:" +j);
         }
    }
}
